#define MAX_ITEMS 10
int quantityOfItemCollected = 0;
bool gameOver = false;
string lastMessage = "";

GameElements elements;
Player player; 

struct GameMap{
    int tiles[25][105];
};

struct VisibleMap {
    bool visible[25][105] = {false}; 
};

struct GameItem {
    int x, y;
    char type;
    bool collected;
};

GameItem gameItems[MAX_ITEMS];
int itemCount = 0;

//função responsavel por sortear a posição x e y dos itens e a % correspondente do type de cada item 
void initItems(GameMap& map) {
    itemCount = 0;
    for (int i = 0; i < MAX_ITEMS; i++) {
        int x, y;
        do {
            x = rand() % 105;
            y = rand() % 25;
        } while (map.tiles[y][x] != 0); // 1 = parede, 0 = caminho
        
        gameItems[i].x = x;
        gameItems[i].y = y;
        gameItems[i].collected = false;
        
        // 40% poção, 40% armadilha, 20% chave
        int r = rand() % 10;
        if (r < 4) {
            gameItems[i].type = elements.item; // Poção
        } else if (r < 8) {
            gameItems[i].type = elements.trap; // Armadilha
        } else {
            gameItems[i].type = elements.key; // Chave
        }
        
        itemCount++;
    }
}
//função responsavel mostrar itens 
void drawItems(VisibleMap& vmap) {
    for (int i = 0; i < itemCount; i++) {
        if (!gameItems[i].collected && vmap.visible[gameItems[i].y][gameItems[i].x]) { 
            COORD coord;
            coord.X = (SHORT)gameItems[i].x;
            coord.Y = (SHORT)gameItems[i].y;
            SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE), coord);
            cout << gameItems[i].type;
        }
    }
}

//função responsavel coletar o item pelo jogador e a logica de cada item
void checkItems(GameMap& map, int playerX, int playerY) {

    for (int i = 0; i < itemCount; i++) {
        if (!gameItems[i].collected && gameItems[i].x == playerX && gameItems[i].y == playerY) {
            gameItems[i].collected = true;

            quantityOfItemCollected++; // itens coletados

            // base para o game over, FALTA TERMINAR 
            if(player.life <= 0) {
                gameOver = true; 
                lastMessage = "Você morreu!";
            } 

            // item para o inventario
            if (gameItems[i].type == elements.item) { 

                // if (player.life < 5) {
                //     player.life += 1; 
                //     lastMessage = "Você pegou uma poção! +1 de vida!";
                // } else {
                //     lastMessage = "Você já está com vida máxima!";
                // }
            }

            // logica para as armadilhas
            else if (gameItems[i].type == elements.trap) {
                int effect = rand() % 5; 

                switch(effect) {
                    case 0: 
                        player.life -= 1;
                        lastMessage = "Você pisou numa armadilha! -1 de vida!";
                        break;
                    case 1: 
                        player.life -= 2;
                        lastMessage = "Armadilha perigosa! -2 de vida!";
                        break;
                    case 2: 
                        player.life += 1;
                        lastMessage = "Armadilha inofensiva! +1 de vida!";
                        break;
                    case 3: 
                        lastMessage = "Você escapou da armadilha sem dano!";
                        break;
                    case 4: 
                        if (player.key > 0) {
                            player.key -= 1;
                            lastMessage = "A armadilha fez você perder uma chave!";
                        } else {
                            lastMessage = "A armadilha tentou pegar uma chave, mas você não tem nenhuma!";
                        }
                        break;
                }
            } 
            // logica para as chaves 
            else if (gameItems[i].type == elements.key) {
                player.key += 1;
                lastMessage = "Você pegou uma chave! +1 chave!";

                // logica para abrir as portas quando tiver chave
                if(player.key > 0) {
                    for (int y = 0; y < 25; y++) {
                        for (int x = 0; x < 105; x++) {
                            if (map.tiles[y][x] == 2) { // 2 = porta
                                map.tiles[y][x] = 0;   // transforma em caminho
                                lastMessage = "Você abriu as portas! Você pode passar pelo icone: #";
                            }
                        }
                    }
                } 
            }
        }
    }
}
