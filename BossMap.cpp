void bossRoom(BossMap& map, int playerX, int playerY){
    
    int rows = 25;  
    int cols = 25;

    for (int i = 0; i < rows; ++i) {
        for (int j = 0; j < cols; ++j) {
            if (i == playerY && j == playerX) {
                cout << person; 
            }else if(map.tiles[i][j] != 1 && map.tiles[i][j] != 0){
                cout << bossIcon; 
            } else {
                cout << (map.tiles[i][j] == 1 ? wall : path); 
            }
        }
        cout << '\n';
    }
    
}